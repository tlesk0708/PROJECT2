// แสดงกล่องข้อมูล
function showInfo() {
    const infoBox = document.getElementById("info-box");
    infoBox.style.display = "block"; // เปลี่ยนการแสดงผล
}

// ซ่อนกล่องข้อมูล
function closeInfo() {
    const infoBox = document.getElementById("info-box");
    infoBox.style.display = "none"; // ซ่อนกล่องข้อมูล
}